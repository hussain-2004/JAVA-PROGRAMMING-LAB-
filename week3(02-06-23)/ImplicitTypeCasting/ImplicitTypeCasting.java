class ImplicitTypeCasting {
    public static void main(String args[]){
        int c = 100000;
        float e = c;
        double f = e;
        //implicit type casting.
        System.out.println("c = "+c);
        System.out.println("e = "+e);
        System.out.println("f = "+f);
    }
}
